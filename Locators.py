import time
from selenium import webdriver
from selenium.webdriver.common.by import By
from selenium.webdriver.support.select import Select

driver = webdriver.Chrome()
driver.get("https://rahulshettyacademy.com/angularpractice/")

#Selenium encuentra elementos por: ID, Xpath, CSSSelector, Classname, Name, linkText
#Se tiene que inspeccionar el elemento para ver la informaciÃ³n listada.
driver.find_element(By.NAME, "email").send_keys("della_zyr@gmail.com")
driver.find_element(By.ID, "exampleInputPassword1").send_keys("qwerty")
driver.find_element(By.ID, "exampleCheck1").click()

#Xpath - //tagname[@attribute='value'] -> //input[@type='submit']
#CSS - tagname[attribute='value'] -> //input[@type='submit'], #id, .classname
driver.find_element(By.XPATH, "//input[@type='submit']").click()
driver.find_element(By.CSS_SELECTOR, "#inlineRadio1").click()
driver.find_element(By.CSS_SELECTOR, "input[name='name']").send_keys("Della Zyr")
message = driver.find_element(By.CLASS_NAME, "alert-success").text
print(message)

assert "Success" in message


driver.find_element(By.XPATH, "(//input[@type='text'])[3]").send_keys("Helloagain")
